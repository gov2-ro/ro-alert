function t(key) {
  const lang = (window.state && state.lang) || "ro";
  return (I18N[lang] && I18N[lang][key]) || (I18N.ro[key]) || key;
}

/* Apply translations to any element carrying data-i18n / data-i18n-ph. */
function applyI18n() {
  document.documentElement.lang = (window.state && state.lang) || "ro";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
}
